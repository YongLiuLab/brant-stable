                    ...'Delete Timepoints';...
                    'Head Motion Est';...
                    'Visual Check';...
                    'TSNR';...
                    'ROI Coordinates';...
                    'Extract Value';...
                    };
    case 'NET'
        prompt = {  'Network Calculation';...
                    'Network Statistics';...
                    };
    case 'VIEW'
        prompt = {  'Surface Mapping';...
                    'ROI Mapping';...
                    'Network Visualization'};
